<h1>Tutorial</h1>

<div id="new_news"><p><b>REJESTRACJA I PROFIL</b><br/><br/>

Musisz być studentem wykładowcy używającego SPOJCLONE (Dominatrix2000). Rejestracja osób zewnętrznych nie jest możliwa. Wykładowca prowadzący zajęcia zakłada konta dla osób ze swoich grup. Po tym jak wykładowca założy już konta użytkownikom, możliwe będzie zalogowanie się na serwisie i rozpoczęcie pracy.<br/>
</p>
</div><br/>
<div id="new_news"><p><b>WYBÓR PROBLEMU DO ROZWIĄZANIA</b><br/><br/>

Zadania do rozwiązania znajdują się w zakładce "Zadania"<br/><br/>

Każde zadanie posiada swoje:<br/><br/>

    ID - numer zadania<br/>
    nazwa zadania - nazwa danego zadania<br/>
    punkty - ilość punktów jakie można otrzymać za prawidłowo rozwiązane zadanie<br/>
    data - data wstawienia danego zadania<br/>
</p>
</div><br/>
<div id="new_news"><p><b>NADSYŁANIE ROZWIĄZAŃ</b><br/><br/>

Zerknijmy na pierwszy problem na SPOJCLONE o nazwie Life, the Universe, and Everything<br/><br/>

Każde zadanie składa się z:<br/><br/>

1) Opisu problemu<br/>
2) Przykładu<br/>
3) Przykładowego wejścia<br/>
4) Przykładowego wyjścia<br/>
5) Dodatkowych informacji<br/><br/>

Aby napisać rozwiązanie, użyj własnego edytora bądź IDE. Następnie aby wysłać rozwiązanie, kliknij odpowiedni link i wklej kod źródłowy rozwiązania do pola tekstowego, ablo załaduj kod z pliku. Z rozwijanej listy wybierz zadanie. Następnie wybierz język rozwiązania i wciśnij przycisk "Wyślij rozwiązanie". <br/>
</p>
</div><br/>
<div id="new_news"><p><b>RANKING</b><br/><br/>

Każdy z uczestników może zobaczyć swój dorobek punktowy w rankingu. Punkty za rozwiązanie zadania przydzielane są w zależności od zadania.<br/>
</p>
</div><br/>
<div id="new_news"><p><b>ROZWIĄZANIA</b><br/><br/>

W zakładce "Rozwiązania" można zobaczyć kto nadesłał już jakieś rozwiązania. Są tam pola takie jak:<br/><br/>

1) Autor<br/>
2) Zadanie<br/>
3) Język<br/>
4) Data<br/>
5) Punkty<br/>
6) Zakończony<br/>
</p>
</div><br/>
