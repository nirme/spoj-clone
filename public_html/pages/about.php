<h1>Czym jest spojclone (Dominatrix2000)</h1>
<p>

<b>SPOJCLONE (Dominatrix2000)</b> jest serwisem przeznaczonym do rozwiązywania problemów programistycznych, online-owym sędzią, akceptującym rozwiązania w wielu językach programowania, stworzonym do wspomagania pracy wykładowcy i jego studentów. 

</p>